// Operadores lógicos (logical operators)

// - 2 valores booleanos, quando verificados,
// resultará em verdadeiro ou falso

let pao = true
let queijo = false

// AND && - ambos precisam ser verdadeiros
console.log(pao && queijo) 

// OR || - somente um precisa ser um verdadeiro
console.log(pao || queijo)

// NOT ! - trocar de valor booleanos
console.log(!pao)